export const getCorrelationData = (data) => {
  const features = ['study_hours_per_day', 'attendance_pct', 'assignments_completed_pct', 'previous_semester_marks', 'class_participation', 'final_score'];
  const featureLabels = ['Study Hours', 'Attendance', 'Assignments', 'Previous Marks', 'Class Particip.', 'Final Score'];

  const correlation = [];

  for (let i = 0; i < features.length; i++) {
    for (let j = 0; j < features.length; j++) {
      const x = data.map(d => d[features[i]]);
      const y = data.map(d => d[features[j]]);
      const corr = calculateCorrelation(x, y);
      correlation.push({
        x: featureLabels[i],
        y: featureLabels[j],
        value: corr
      });
    }
  }

  return correlation;
};

const calculateCorrelation = (x, y) => {
  const n = x.length;
  const sumX = x.reduce((a, b) => a + b, 0);
  const sumY = y.reduce((a, b) => a + b, 0);
  const sumXY = x.reduce((total, xi, i) => total + xi * y[i], 0);
  const sumX2 = x.reduce((total, xi) => total + xi * xi, 0);
  const sumY2 = y.reduce((total, yi) => total + yi * yi, 0);

  const numerator = n * sumXY - sumX * sumY;
  const denominator = Math.sqrt((n * sumX2 - sumX * sumX) * (n * sumY2 - sumY * sumY));

  if (denominator === 0) return 0;
  return (numerator / denominator).toFixed(2);
};

