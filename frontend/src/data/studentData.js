export const calculateStats = (data) => {
  const totalRecords = data.length;
  const avgMarks = data.reduce((sum, s) => sum + s.final_score, 0) / totalRecords;
  const avgAttendance = data.reduce((sum, s) => sum + s.attendance_pct, 0) / totalRecords;

  return {
    totalRecords: Math.round(totalRecords),
    avgMarks: avgMarks.toFixed(1),
    avgAttendance: avgAttendance.toFixed(1)
  };
};

export const getAttendanceDistribution = (data) => {
  const bins = [0, 20, 40, 60, 80, 100];
  const labels = ['0-20', '20-40', '40-60', '60-80', '80-100'];
  const distribution = labels.map(() => 0);

  data.forEach(student => {
    const att = student.attendance_pct;
    for (let i = 0; i < bins.length - 1; i++) {
      if (att >= bins[i] && att < bins[i + 1]) {
        distribution[i]++;
        break;
      }
    }
    if (att === 100) distribution[bins.length - 2]++;
  });

  return labels.map((label, i) => ({ range: label, count: distribution[i] }));
};

