export const trainModel = (data) => {
  // Simple linear regression - using least squares
  // Features: study_hours, attendance, assignments, previous_marks, class_participation
  // Target: final_score
  const features = ['study_hours_per_day', 'attendance_pct', 'assignments_completed_pct', 'previous_semester_marks', 'class_participation'];

  // Calculate means
  const n = data.length;
  const means = {};
  features.forEach(f => {
    means[f] = data.reduce((sum, row) => sum + row[f], 0) / n;
  });
  const meanY = data.reduce((sum, row) => sum + row.final_score, 0) / n;

  // Calculate coefficients using simple linear regression
  const coefficients = {};
  features.forEach(f => {
    let numerator = 0;
    let denominator = 0;
    data.forEach(row => {
      numerator += (row[f] - means[f]) * (row.final_score - meanY);
      denominator += (row[f] - means[f]) ** 2;
    });
    coefficients[f] = denominator !== 0 ? numerator / denominator : 0;
  });

  // Calculate intercept
  let intercept = meanY;
  features.forEach(f => {
    intercept -= coefficients[f] * means[f];
  });

  return { coefficients, intercept };
};

export const predictScore = (input) => {
  const { coefficients, intercept } = trainModel(studentData);

  const features = {
    studyHours: input.studyHours,
    attendance: input.attendance,
    assignments: input.assignments,
    previousMarks: input.previousMarks,
    classParticipation: input.classParticipation
  };

  // Calculate prediction
  let score = intercept;
  score += coefficients.study_hours_per_day * features.studyHours;
  score += coefficients.attendance_pct * features.attendance;
  score += coefficients.assignments_completed_pct * features.assignments;
  score += coefficients.previous_semester_marks * features.previousMarks;
  score += coefficients.class_participation * features.classParticipation;

  // Round to nearest integer and clamp between 0-100
  score = Math.round(Math.max(0, Math.min(100, score)));

  // Determine grade
  let grade, tip;
  if (score >= 90) {
    grade = 'A';
    tip = '🌟 Excellent! Keep up the great work!';
  } else if (score >= 80) {
    grade = 'B';
    tip = '👍 Great job! You\'re performing well.';
  } else if (score >= 70) {
    grade = 'C';
    tip = '💪 Good effort! Try to improve your study habits.';
  } else if (score >= 60) {
    grade = 'D';
    tip = '📚 Consider increasing study hours and attendance.';
  } else {
    grade = 'F';
    tip = '⚠️ Needs improvement! Focus on attendance and assignments.';
  }

  return { score, grade, tip };
};